package org.unism.gm.action.form;

public class DevFaultSearchForm {
	
	private String dev_no;
	
	private String def_grade;
	
	private String def_object;
	
	private String def_type;
	
	private String def_symptom;
	
	private String def_state;

	public String getDev_no() {
		return dev_no;
	}

	public void setDev_no(String devNo) {
		dev_no = devNo;
	}

	public String getDef_grade() {
		return def_grade;
	}

	public void setDef_grade(String defGrade) {
		def_grade = defGrade;
	}

	public String getDef_type() {
		return def_type;
	}

	public void setDef_type(String defType) {
		def_type = defType;
	}

	public String getDef_state() {
		return def_state;
	}

	public void setDef_state(String defState) {
		def_state = defState;
	}

	public String getDef_object() {
		return def_object;
	}

	public void setDef_object(String def_object) {
		this.def_object = def_object;
	}

	public String getDef_symptom() {
		return def_symptom;
	}

	public void setDef_symptom(String def_symptom) {
		this.def_symptom = def_symptom;
	}
}
