package org.unism.gm.action.form;

public class HisFindForm {
	
	private Double[] oriValue;
	
	private Double[] corrValue;
	
	public HisFindForm(int x) {
		this.oriValue = new Double[x];
		this.corrValue = new Double[x];
	}
	
	public Double[] getOriValue() {
		return oriValue;
	}

	public void setOriValue(Double[] oriValue) {
		this.oriValue = oriValue;
	}

	public Double[] getCorrValue() {
		return corrValue;
	}

	public void setCorrValue(Double[] corrValue) {
		this.corrValue = corrValue;
	}
}
